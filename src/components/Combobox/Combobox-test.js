import React from 'react';
import Combobox from '../Combobox';
import { mount } from 'enzyme';

describe('Combobox', () => {
  it('should render');

  describe('with no input', () => {
    it('should display the given placeholder');

    it('should show all items when interacting with the dropdown');
  });

  describe('with input', () => {
    it('should display the current input');

    it('should show a dropdown list of matching options');

    it('should show all items when toggling the dropdown list');

    it('should clear the input when the clear icon is interacted with');
  });

  describe('when disabled', () => {
    it('should not update the input');

    it('should not display the dropdown list');
  });

  describe('with pre-selected item', () => {});
});
